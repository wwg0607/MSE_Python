if True :                  # 조건문 사용
    if False:              # If 가 True일때 >>  False일경우 1,2 출력 그 외 3 출력
        print("1")
        print("2")
    else:
        print("3")
else :                     # 그 외일때 >> 4츌력
    print("4")
print("5")                 # 5를 출력
                           # 따라서 T임으로 F가 아닌 else에 해당하고 5를 출력해야함으로 3,5가 출력된다. 

